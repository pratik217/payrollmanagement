    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/payroll", "root", "");
            if (!con.isClosed()) {
                String id = userid.getText();
                String pwd = password.getText();
                ps = con.prepareStatement("select * from adminlogin where userid=? and password=?");
                ps.setString(1, id);
                ps.setString(2, pwd);
                rs = ps.executeQuery();

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "You have been successfully logged in!");
                    new menu().setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid userid or password");
                    userid.setText("");
                    password.setText("");
                }
            }

            ps.close();
            rs.close();

        } catch (SQLException | HeadlessException | ClassNotFoundException e) {
            System.out.println("Error in Mysql Connection");
        }
    }//GEN-LAST:event_loginActionPerformed

